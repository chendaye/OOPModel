<?php
    /**
     * 什么是模式？
     * 现在处理的问题大部分都已被其他程序员处理了
     * 模式提取了共同的问题；定义了经过测试的解决方案，并描述了可能结果
     * 模式就是特定环境下同类问题的解决方案
     *
     * 设计模式就是分析过的问题和问题解决方案所阐述的优秀实践
     * 设计模式记录并规范了这些问题的解决方案
     *
     * 模式是类和对象的一种特殊组织形式，目的是定义定义解决方案的应用条件，效果
     *
     * 问题及问题发生的环境都是模式的基础
     * 找出问题比使用模式更难，要避免模式滥用
     *
     * 为什么使用模式？、
     * 模式是对问题的定义，和解决方案的描述，一次应用模式可以解决共性问题
     *
     * 一个模式定义了一个问题，描述了一个解决方案，设计模式和语言无关
     * 模式视为协作而设计的，设计模式促进良好的设计
     *
     * 书《设计模式》
     */
